// Creamos una caja llamada 'nombre' y le ponemos el valor "Ana"
let nombre = "Ana";

// Creamos otra caja llamada 'edad' y le ponemos el valor 16
let edad = 16;

// Ahora podemos usar lo que hay dentro de la caja
console.log("Hola, soy " + nombre + " y tengo " + edad + " años."); // Output: Hola, soy Ana y tengo 16 años.

// ¡Y podemos cambiar el contenido de la caja!
nombre = "Pedro";
edad = 17;

console.log("Ahora soy " + nombre + " y tengo " + edad + " años."); // Output: Ahora soy Pedro y tengo 17 años.
